# Kurzbeschreibung von Listen,  Dictionary und Tuple

# hier wird eine Liste erstellt
listendings = ["Apfel", "Birne", "Zitrone"]
print(listendings)
print(type(listendings))


# das ist ein Dictionary, hier gibt es immer ein Paar von Werten
listendings_2 = {"Hello": "Hallo", "Apple":"Apfel"}
print(listendings_2)
print(type(listendings_2))

# das ist ein Tuple und aehnlich wie die Liste, es kann aber nichts veraendert werden
listendings_3 = ("Apfel", "Birne", "Zitrone")
print(listendings_3)
print(type(listendings_3))

