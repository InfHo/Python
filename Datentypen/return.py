# die Funktion gibt mit 'return' einen Wert aus, hier zB text1+text2
def text_bastel_kasten(text1,text2):
    return text1+text2

# hier werden zwei Zahlen eingegeben
text_bastel_kasten(3, 4)

# mit dem return wert der Funktion kann auch eine Variable festgelegt werden
x = text_bastel_kasten(5, 6)

#hier kann man den Wert der Funktion zeigen
print(text_bastel_kasten(3, 4))

# oder hier die Variable, die mit der Funktion mit zwei Werten beschrieben wird
print(x)